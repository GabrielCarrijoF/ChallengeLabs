public class Product {

  int productId;
  double value;

  public int getProductId() {
    return productId;
  }

  public void setProductId(final int productId) {
    this.productId = productId;
  }

  public double getValue() {
    return value;
  }

  public void setValue(double value) {
    this.value = value;
  }
}
